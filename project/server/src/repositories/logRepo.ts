/**
 * @file Репозиторий логов — слой доступа к данным журнала аудита действий пользователей.
 *
 * Хранит хронологическую запись всех значимых действий: входы в систему,
 * загрузка выписок, создание/удаление сущностей, резервное копирование и т.д.
 * Каждая запись привязана к пользователю (если действие не анонимное).
 */
import { query } from '../db';
import type { LogRow } from '../types';

/**
 * Возвращает последние 200 записей журнала аудита.
 *
 * LEFT JOIN с users для отображения логина и ФИО пользователя.
 * Сортировка по убыванию ID — самые свежие записи сверху.
 *
 * @returns {Promise<LogRow[]>} Массив записей лога с данными пользователя (login, full_name)
 */
export async function findAll(): Promise<LogRow[]> {
  // Выборка логов с данными пользователя через LEFT JOIN.
  // LEFT JOIN сохраняет записи лога, даже если пользователь был удалён.
  // Ограничение в 200 записей для производительности страницы аудита.
  return query<LogRow>(
    'SELECT l.*, u.login, u.full_name FROM logs l LEFT JOIN users u ON l.user_id = u.id ORDER BY l.id DESC LIMIT 200'
  );
}

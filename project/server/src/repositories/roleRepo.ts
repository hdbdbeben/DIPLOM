/**
 * @file Репозиторий ролей — слой доступа к данным справочника ролей пользователей.
 *
 * Роли определяют уровень доступа в системе: администратор, оператор,
 * бухгалтер и т.д. Каждая роль имеет уникальный код и читаемое имя.
 */
import { query } from '../db';
import type { RoleRow } from '../types';

/**
 * Возвращает полный список ролей, отсортированный по идентификатору.
 *
 * Используется при создании/редактировании пользователей для выбора роли,
 * а также при формировании списков с фильтрацией по правам доступа.
 *
 * @returns {Promise<RoleRow[]>} Массив всех записей справочника ролей
 */
export async function findAll(): Promise<RoleRow[]> {
  // Получение всех ролей в порядке возрастания ID
  return query<RoleRow>('SELECT * FROM roles ORDER BY id');
}

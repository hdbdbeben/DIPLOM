/**
 * @file Утилиты парсинга данных банковских выписок.
 * Набор чистых функций для преобразования строковых данных из файлов выписок
 * (формат 1С / банк-клиент) в типизированные значения:
 * - Очистка и приведение денежных сумм к числовому типу.
 * - Извлечение ИНН из текстовых полей контрагентов.
 * - Нормализация имён организаций (удаление маркеров ИНН, кавычек, лишних пробелов).
 * - Приведение дат к формату ISO (YYYY-MM-DD).
 *
 * Все функции чистые — не имеют побочных эффектов и не зависят от внешнего состояния.
 */

/**
 * Преобразует строковое представление денежной суммы в число.
 *
 * Алгоритм:
 * 1. Если значение пустое (и не 0), возвращает 0.
 * 2. Удаляет все символы, кроме цифр, минуса, точки и запятой.
 * 3. Заменяет запятую на точку (российский формат → стандартный).
 * 4. Преобразует в число через parseFloat, fallback — 0.
 *
 * @param {string | number | undefined} val — исходное значение (строка из выписки или число)
 * @returns {number} Числовое значение суммы (0 при ошибке парсинга)
 *
 * @example
 * parseNumber("1 234,56")   // → 1234.56
 * parseNumber("-500.00")    // → -500
 * parseNumber(undefined)    // → 0
 * parseNumber(0)            // → 0
 */
export function parseNumber(val: string | number | undefined): number {
  // !val отсеивает undefined, null, '' — но пропускает 0
  if (!val && val !== 0) return 0;
  // Удаляем всё, что не цифра, минус, точка или запятая;
  // заменяем запятую-разделитель на точку для parseFloat
  return parseFloat(String(val).replace(/[^-0-9.,]/g, '').replace(',', '.')) || 0;
}

/**
 * Извлекает ИНН (10 или 12 цифр) из произвольной текстовой строки.
 *
 * Используется, когда ИНН встроен в имя контрагента в поле выписки
 * (например, "ООО Ромашка ИНН 7728300200").
 *
 * @param {string | undefined} text — текст, предположительно содержащий ИНН
 * @returns {string} Строка из 10 или 12 цифр, либо пустая строка, если ИНН не найден
 *
 * @example
 * extractInn("ООО Ромашка ИНН 7728300200")        // → "7728300200"
 * extractInn("ИНН 771501001234")                   // → "771501001234"
 * extractInn(undefined)                             // → ""
 */
export function extractInn(text: string | undefined): string {
  if (!text) return '';
  // Ищем последовательность из 10 или 12 цифр, ограниченную границами слова
  const m = text.match(/\b(\d{10}|\d{12})\b/);
  return m ? m[1] : '';
}

/**
 * Нормализует наименование организации — удаляет мусорные маркеры
 * и приводит форматирование к единому виду.
 *
 * Выполняет:
 * 1. Удаляет маркер "ИНН" (если он остался в имени после извлечения ИНН).
 * 2. Заменяет кавычки-ёлочки («») на прямые кавычки ("").
 * 3. Схлопывает множественные пробелы в один.
 * 4. Удаляет пробелы в начале и конце строки.
 *
 * @param {string | undefined} text — исходное имя контрагента из выписки
 * @returns {string} Очищенное имя
 *
 * @example
 * extractName("ООО «Ромашка» ИНН 7728300200")  // → 'ООО "Ромашка"'
 * extractName("  АО   Флант  ")                 // → "АО Флант"
 */
export function extractName(text: string | undefined): string {
  if (!text) return '';
  return text.replace(/ИНН\s*/i, '').replace(/["«»]/g, '"').replace(/\s+/g, ' ').trim();
}

/**
 * Приводит дату из российского формата (ДД.ММ.ГГГГ) к ISO-формату (ГГГГ-ММ-ДД).
 *
 * Логика:
 * - Очищает строку от не-цифровых и не-точечных символов.
 * - Если дата содержит три сегмента через точку:
 *   - Если первый сегмент уже 4-значный (год в начале), возвращает как есть (уже ISO-like).
 *   - Иначе переставляет части: ДД.ММ.ГГГГ → ГГГГ-ММ-ДД.
 * - Если структура не соответствует ДД.ММ.ГГГГ, возвращает очищенную строку.
 *
 * @param {string | undefined} val — строка даты в произвольном формате
 * @returns {string} Дата в формате YYYY-MM-DD, либо пустая строка
 *
 * @example
 * normalizeDate("15.04.2026")    // → "2026-04-15"
 * normalizeDate("2026-04-15")    // → "2026-04-15"
 * normalizeDate(undefined)       // → ""
 */
export function normalizeDate(val: string | undefined): string {
  if (!val) return '';
  // Оставляем только цифры и точки
  const cleaned = val.replace(/[^\d.]/g, '').trim();
  const parts = cleaned.split('.');
  if (parts.length === 3) {
    // Если первая часть уже четырёхзначная — дата в формате YYYY.MM.DD
    if (parts[0].length === 4) return cleaned;
    // Преобразуем ДД.ММ.ГГГГ → ГГГГ-ММ-ДД, дополняя день/месяц нулями до 2 знаков
    return parts[2] + '-' + parts[1].padStart(2, '0') + '-' + parts[0].padStart(2, '0');
  }
  return cleaned;
}
